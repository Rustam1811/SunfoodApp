const admin = require("firebase-admin");

if (!admin.apps.length) {
  // 1) –ü—Ä–æ—á–∏—Ç–∞—Ç—å Base64 –∏–∑ env
  const b64 = process.env.FIREBASE_KEY_BASE64;
  if (!b64) throw new Error("FIREBASE_KEY_BASE64 not set");

  // 2) –†–∞—Å–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å –≤ JSON
  const serviceAccount = JSON.parse(
    Buffer.from(b64, "base64").toString("utf8")
  );

  // 3) –ò—Å–ø—Ä–∞–≤–∏—Ç—å literal "\\n" ‚Üí —Ä–µ–∞–ª—å–Ω—ã–µ "\n"
  if (serviceAccount.private_key) {
    serviceAccount.private_key = serviceAccount.private_key.replace(/\\n/g, "\n");
  }

  // 4) –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å Admin SDK
  admin.initializeApp({
    credential: admin.credential.cert(serviceAccount),
  });
}

const db = admin.firestore();

module.exports = async function handler(req, res) {
  // —Ç–æ—Ç –∂–µ CORS-–±–ª–æ–∫ —á—Ç–æ –∏ –≤ register.js
  const origin = req.headers.origin || "";
  if (["http://localhost:5173", "https://coffee-addict.vercel.app"].includes(origin)) {
    res.setHeader("Access-Control-Allow-Origin", origin);
  }
  res.setHeader("Access-Control-Allow-Methods", "POST,OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");
  if (req.method === "OPTIONS") return res.status(200).end();

  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method Not Allowed" });
  }

  try {
    const { phone, password } = req.body;
    if (!phone || !password) {
      return res.status(400).json({ error: "–ù–æ–º–µ—Ä –∏ –ø–∞—Ä–æ–ª—å –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã" });
    }
    const snapshot = await db.collection("users")
      .where("phone", "==", phone)
      .limit(1)
      .get();
    if (snapshot.empty) {
      return res.status(404).json({ error: "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω" });
    }
    const user = snapshot.docs[0].data();
    if (user.password !== password) {
      return res.status(401).json({ error: "–ù–µ–≤–µ—Ä–Ω—ã–π –ø–∞—Ä–æ–ª—å" });
    }
    return res.status(200).json({ token: "mock-token", user });
  } catch (err) {
    console.error("üî• Login Error:", err);
    return res.status(500).json({ error: "–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞" });
  }
};
